public class datatype {
    public static void main(String[] args) {

        //datatypes different because 0f their size

        /*
         byte - 1 byte [-128 to 127] 
         bool - 1 byte
         char - 2 byte
         short - 2 byte
         int - 4 byte
         float - 4 byte
         double - 8 byte
         long - 8 byte

         */

        int a = 10;
        System.out.println(a); 
        //long - badi integer value

        char ch = 'A';
        System.out.println(ch);
        
        boolean var = true;
        System.out.println(var);

        float price = 10.5f;                    //end me f is compulsory to put
        System.out.println(price);
        //double - badi decimal value



        //not used much - int se choti size h 
        byte b = 127;               //maximum is 127 cant be more than that
        System.out.println(b);
        short c = 24;
        System.out.println(c);
    }
}
